    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        try
        {
            pstmt=conn.prepareStatement("insert into STUDENT (USN, STUDENT_NAME) values (?,?)");
            pstmt.setString(1,usn.getText());
            pstmt.setString(2,stud_name.getText());
            pstmt.execute();
            JOptionPane.showMessageDialog(null,"Student details added successfully!!!!");
            setVisible(false);
            Home_Page ob = new Home_Page();
            ob.setVisible(true);
        }
        catch(HeadlessException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnregisterActionPerformed

