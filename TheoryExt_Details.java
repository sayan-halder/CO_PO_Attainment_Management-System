    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        try
        {
            pstmt=conn.prepareStatement("insert into THEORY_EXTERNAL (SUBJECT_CODE, USN, GRADE_POINT) values (?,?,?)");
            pstmt.setString(1,subcode.getText());
            pstmt.setString(2,usn.getText());
            pstmt.setString(3,gradepoint.getText());
            pstmt.execute();
            
            pstmt=conn.prepareStatement("update THEORY_EXTERNAL set MARKS = (GRADE_POINT - 0.75) * 10\n" +
                                        "where SUBJECT_CODE=? and USN=?");
            pstmt.setString(1,subcode.getText());
            pstmt.setString(2,usn.getText());
            pstmt.execute();
            
            pstmt=conn.prepareStatement("select MARKS from THEORY_EXTERNAL where SUBJECT_CODE=? and USN=?");
            pstmt.setString(1, subcode.getText());
            pstmt.setString(2, usn.getText());
            rs=pstmt.executeQuery();

            if(rs.next())
            {
                String m=rs.getString("marks");
                marks.setText(m);
            }           

            JOptionPane.showMessageDialog(null,"Marks for theory external added successfully!!!!");
        }
        catch(HeadlessException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnsubmitActionPerformed

